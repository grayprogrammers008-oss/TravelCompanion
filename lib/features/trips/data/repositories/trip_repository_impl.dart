import '../../../../shared/models/trip_model.dart';
import '../../../../core/network/supabase_client.dart';
import '../../domain/repositories/trip_repository.dart';
import '../../domain/usecases/get_user_stats_usecase.dart';
import '../datasources/trip_remote_datasource.dart';

/// Implementation of trip repository using Supabase as the data source
class TripRepositoryImpl implements TripRepository {
  final TripRemoteDataSource _remoteDataSource;

  TripRepositoryImpl(this._remoteDataSource);

  @override
  Future<TripModel> createTrip({
    required String name,
    String? description,
    String? destination,
    DateTime? startDate,
    DateTime? endDate,
    String? coverImageUrl,
    double? cost,
    String? currency,
    bool isPublic = true,
  }) async {
    try {
      final trip = TripModel(
        id: '', // Will be generated by backend
        name: name,
        description: description,
        destination: destination,
        startDate: startDate,
        endDate: endDate,
        coverImageUrl: coverImageUrl,
        createdBy: '', // Will be set by backend
        cost: cost,
        currency: currency ?? 'INR',
        isPublic: isPublic,
      );

      final createdTrip = await _remoteDataSource.createTrip(trip);

      // Send notification to other members (if any join later, they'll get notified via member_added)
      // Note: For now, only creator exists, so notification will be sent when members are added

      return createdTrip;
    } catch (e) {
      throw Exception('Failed to create trip: $e');
    }
  }

  @override
  Future<List<TripWithMembers>> getUserTrips() async {
    try {
      return await _remoteDataSource.getUserTrips();
    } catch (e) {
      throw Exception('Failed to get user trips: $e');
    }
  }

  @override
  Future<TripWithMembers> getTripById(String tripId) async {
    try {
      final trip = await _remoteDataSource.getTripById(tripId);
      if (trip == null) {
        throw Exception('Trip not found');
      }
      return trip;
    } catch (e) {
      throw Exception('Failed to get trip: $e');
    }
  }

  @override
  Future<TripModel> updateTrip({
    required String tripId,
    String? name,
    String? description,
    String? destination,
    DateTime? startDate,
    DateTime? endDate,
    String? coverImageUrl,
    bool? isCompleted,
    DateTime? completedAt,
    double? rating,
    double? cost,
    String? currency,
    bool? isPublic,
  }) async {
    try {
      // Build updates map
      final updates = <String, dynamic>{};
      String? updatedField;

      if (name != null) {
        updates['name'] = name;
        updatedField = 'name';
      }
      if (description != null) {
        updates['description'] = description;
        updatedField = updatedField == null ? 'description' : 'details';
      }
      if (destination != null) {
        updates['destination'] = destination;
        updatedField = updatedField == null ? 'destination' : 'details';
      }
      if (startDate != null || endDate != null) {
        if (startDate != null) updates['startDate'] = startDate;
        if (endDate != null) updates['endDate'] = endDate;
        updatedField = updatedField == null ? 'dates' : 'details';
      }
      if (coverImageUrl != null) {
        updates['coverImageUrl'] = coverImageUrl;
        updatedField = updatedField == null ? 'cover image' : 'details';
      }
      if (isCompleted != null) {
        updates['isCompleted'] = isCompleted;
        updatedField = updatedField == null ? 'completion status' : 'details';
      }
      if (completedAt != null) {
        updates['completedAt'] = completedAt.toIso8601String();
        updatedField = updatedField == null ? 'completion date' : 'details';
      }
      if (rating != null) {
        updates['rating'] = rating;
        updatedField = updatedField == null ? 'rating' : 'details';
      }
      if (cost != null) {
        updates['cost'] = cost;
        updatedField = updatedField == null ? 'cost' : 'details';
      }
      if (currency != null) {
        updates['currency'] = currency;
        updatedField = updatedField == null ? 'currency' : 'details';
      }
      if (isPublic != null) {
        updates['isPublic'] = isPublic;
        updatedField = updatedField == null ? 'visibility' : 'details';
      }

      await _remoteDataSource.updateTrip(tripId, updates);

      // Fetch and return updated trip
      final updatedTrip = await _remoteDataSource.getTripById(tripId);
      if (updatedTrip == null) {
        throw Exception('Failed to fetch updated trip');
      }

      // Send notification (handled by Supabase trigger/edge function)
      // The notification service will be called via database trigger

      return updatedTrip.trip;
    } catch (e) {
      throw Exception('Failed to update trip: $e');
    }
  }

  @override
  Future<void> deleteTrip(String tripId) async {
    try {
      await _remoteDataSource.deleteTrip(tripId);
    } catch (e) {
      throw Exception('Failed to delete trip: $e');
    }
  }

  @override
  Future<List<TripMemberModel>> getTripMembers(String tripId) async {
    try {
      final trip = await _remoteDataSource.getTripById(tripId);
      if (trip == null) {
        throw Exception('Trip not found');
      }
      return trip.members;
    } catch (e) {
      throw Exception('Failed to get trip members: $e');
    }
  }

  @override
  Future<TripMemberModel> addMember({
    required String tripId,
    required String userId,
    String role = 'member',
  }) async {
    try {
      await _remoteDataSource.addMember(tripId, userId, role: role);

      // Fetch and return the added member
      final trip = await _remoteDataSource.getTripById(tripId);
      final member = trip?.members.firstWhere(
        (m) => m.userId == userId,
        orElse: () => throw Exception('Member not found after adding'),
      );
      if (member == null) throw Exception('Member not found');

      // Send notification to other trip members
      // (handled by Supabase trigger/edge function)

      return member;
    } catch (e) {
      throw Exception('Failed to add member: $e');
    }
  }

  @override
  Future<void> removeMember({
    required String tripId,
    required String userId,
  }) async {
    try {
      await _remoteDataSource.removeMember(tripId, userId);
    } catch (e) {
      throw Exception('Failed to remove member: $e');
    }
  }

  @override
  Stream<List<TripWithMembers>> watchUserTrips() {
    try {
      // Delegate to remote datasource which has real-time implementation
      return _remoteDataSource.watchUserTrips();
    } catch (e) {
      throw Exception('Failed to watch user trips: $e');
    }
  }

  @override
  Stream<TripWithMembers> watchTrip(String tripId) {
    try {
      // Delegate to remote datasource which has real-time implementation
      return _remoteDataSource.watchTrip(tripId);
    } catch (e) {
      throw Exception('Failed to watch trip: $e');
    }
  }

  @override
  Future<UserTravelStats> getUserStats() async {
    try {
      final stats = await _remoteDataSource.getUserStats();
      return stats;
    } catch (e) {
      throw Exception('Failed to get user stats: $e');
    }
  }

  @override
  Stream<UserTravelStats> watchUserStats() {
    try {
      // Delegate to remote datasource which has real-time implementation
      return _remoteDataSource.watchUserStats();
    } catch (e) {
      throw Exception('Failed to watch user stats: $e');
    }
  }

  @override
  Future<List<TripWithMembers>> getDiscoverableTrips() async {
    try {
      return await _remoteDataSource.getDiscoverableTrips();
    } catch (e) {
      throw Exception('Failed to get discoverable trips: $e');
    }
  }

  @override
  Future<void> joinTrip(String tripId) async {
    try {
      // Get current user ID
      final userId = SupabaseClientWrapper.currentUserId;
      if (userId == null) {
        throw Exception('User not authenticated');
      }

      // Add user as a member with 'member' role
      await _remoteDataSource.addMember(tripId, userId, role: 'member');
    } catch (e) {
      throw Exception('Failed to join trip: $e');
    }
  }

  @override
  Future<String> copyTrip({
    required String sourceTripId,
    required String newName,
    required DateTime newStartDate,
    required DateTime newEndDate,
    bool copyItinerary = true,
    bool copyChecklists = true,
  }) async {
    try {
      return await _remoteDataSource.copyTrip(
        sourceTripId: sourceTripId,
        newName: newName,
        newStartDate: newStartDate,
        newEndDate: newEndDate,
        copyItinerary: copyItinerary,
        copyChecklists: copyChecklists,
      );
    } catch (e) {
      throw Exception('Failed to copy trip: $e');
    }
  }

  @override
  Future<bool> toggleFavorite(String tripId) async {
    try {
      return await _remoteDataSource.toggleFavorite(tripId);
    } catch (e) {
      throw Exception('Failed to toggle favorite: $e');
    }
  }

  @override
  Future<List<String>> getFavoriteTripIds() async {
    try {
      return await _remoteDataSource.getFavoriteTripIds();
    } catch (e) {
      throw Exception('Failed to get favorite trip IDs: $e');
    }
  }
}
